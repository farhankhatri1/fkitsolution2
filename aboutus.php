        const cursor = document.querySelector('.custom-cursor');
        const dot = document.querySelector('.custom-dot');

        let mouseX = 0, mouseY = 0;
        let cursorX = 0, cursorY = 0;

        window.addEventListener('mousemove', e => {
            mouseX = e.clientX;
            mouseY = e.clientY;
            dot.style.left = mouseX + 'px';
            dot.style.top = mouseY + 'px';
        });

        function animate() {
            cursorX += (mouseX - cursorX) * 0.1;
            cursorY += (mouseY - cursorY) * 0.1;

            cursor.style.left = cursorX + 'px';
            cursor.style.top = cursorY + 'px';

            requestAnimationFrame(animate);
        }

        animate();

        // counter js
       
const counters = document.querySelectorAll('.stat-number');

const runCounters = () => {
  const totalFrames = 60;

  counters.forEach(counter => {
    counter.innerText = "0"; // Reset to 0
    const target = +counter.getAttribute('data-target');
    let frame = 0;

    const updateCount = () => {
      frame++;
      const progress = frame / totalFrames;

      // Interpolate smoothly to 5 (or your custom logic)
      const isCustom = target === 5;
      const displayValue = isCustom ? 8 : target;

      const current = Math.ceil(displayValue * progress);

      if (frame < totalFrames) {
        counter.innerText = current;
        requestAnimationFrame(updateCount);
      } else {
        // ✅ Final value
        if (isCustom) {
          counter.innerText = "8y";
        } else {
          counter.innerText = target + "+";
        }
      }
    };

    updateCount();
  });
};


  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        runCounters(); // Trigger when visible
      }
    });
  }, {
    threshold: 0.5
  });

  const statsWrapper = document.querySelector('.stats-wrapper');
  if (statsWrapper) {
    observer.observe(statsWrapper);
  }

